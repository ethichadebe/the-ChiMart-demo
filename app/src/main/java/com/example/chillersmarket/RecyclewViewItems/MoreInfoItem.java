package com.example.chillersmarket.RecyclewViewItems;

public class MoreInfoItem {
    private String strHead, strBody;

    public MoreInfoItem(String strHead, String strBody) {
        this.strHead = strHead;
        this.strBody = strBody;
    }

    public String getStrHead() {
        return strHead;
    }

    public void setStrHead(String strHead) {
        this.strHead = strHead;
    }

    public String getStrBody() {
        return strBody;
    }

    public void setStrBody(String strBody) {
        this.strBody = strBody;
    }
}
